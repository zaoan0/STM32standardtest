    f.write(b'\n')
    f.write(b'static void DrawBackBtn(void)\n{\n')
    f.write(b'    back_btn.x = BACK_BTN_X; back_btn.y = BACK_BTN_Y;\n')
    f.write(b'    back_btn.w = BACK_BTN_W; back_btn.h = BACK_BTN_H;\n')
