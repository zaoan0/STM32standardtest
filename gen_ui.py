#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""Generate ui.c with GB2312 encoded Chinese strings."""

# Define all GB2312 byte sequences for Chinese strings
CN_TITLE        = b'\xD6\xB1\xCF\xDF\xD1\xAD\xBC\xA3\xD0\xA1\xB3\xB5 v2.0'  # 直线循迹小车 v2.0
