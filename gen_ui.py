    f.write(b'            track_mode_btns[i].x = sx + i * (TRACK_BTN_W + TRACK_BTN_GAP);\n')
    f.write(b'            track_mode_btns[i].y = TRACK_BTN_Y;\n')
    f.write(b'            track_mode_btns[i].w = TRACK_BTN_W;\n')
