    f.write(b'                return;\n            }\n        }\n    }\n')
    f.write(b'    /* \xBF\xEC\xCB\xD9\xC2\xFD\xCA\xB1\xB0\xE6 */\n')
    f.write(b'    for (i = 0; i < 3; i++) {\n')
    f.write(b'        if (PtInRect(tx, ty, &track_preset_btns[i])) {\n')
    f.write(b'            Tracking_SetBaseSpeed(track_preset_vals[i][0]);\n')
    f.write(b'            Tracking_SetSlightSpeed(track_preset_vals[i][1]);\n')
