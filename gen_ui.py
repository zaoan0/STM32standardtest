    f.write(b'    LCD_Clear(BG_COLOR);\n    DrawBackBtn();\n')
    f.write(b'    LCD_ShowMixedString(340, 20, ' + b(CN_TRACK_MODE) + b', TITLE_COLOR, BG_COLOR);\n')
