    f.write(b'    LCD_ShowMixedString(280, 20, ' + b(CN_TITLE) + b', TITLE_COLOR, BG_COLOR);\n')
    f.write(b'    LCD_ShowMixedString(340, 50, ' + b(CN_SELECT_MODE) + b', LIGHTGRAY, BG_COLOR);\n')
