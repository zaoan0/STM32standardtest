    f.write(b'            LCD_DrawButtonCN(track_mode_btns[i].x, track_mode_btns[i].y,\n')
    f.write(b'                             track_mode_btns[i].w, track_mode_btns[i].h,\n')
    f.write(b'                             track_mode_names[i], BTN_TEXT, c);\n')
    f.write(b'        }\n    }\n')
    f.write(b'    LCD_ShowMixedString(30, 145, "PARAMS:", PARAM_LABEL, BG_COLOR);\n')
    f.write(b'    for (i = 0; i < TRACK_PARAM_NUM; i++) {\n')
    f.write(b'        uint16_t ly = TRACK_PARAM_Y + i * TRACK_PARAM_ROW;\n')
    f.write(b'        track_minus[i].x = TRACK_PARAM_LX;\n        track_minus[i].y = ly;\n')
    f.write(b'        track_minus[i].w = TRACK_PARAM_BTN;\n        track_minus[i].h = TRACK_PARAM_BH;\n')
    f.write(b'        track_plus[i].x = TRACK_PARAM_LX + TRACK_PARAM_BTN + TRACK_PARAM_VW + 15;\n')
    f.write(b'        track_plus[i].y = ly;\n        track_plus[i].w = TRACK_PARAM_BTN;\n')
    f.write(b'        track_plus[i].h = TRACK_PARAM_BH;\n')
    f.write(b'        LCD_ShowMixedString(30, ly + 10, track_param_names[i], PARAM_LABEL, BG_COLOR);\n')
    f.write(b'        LCD_DrawButtonCN(track_minus[i].x, track_minus[i].y,\n')
    f.write(b'                         track_minus[i].w, track_minus[i].h, "-", BTN_TEXT, DARKGRAY);\n')
    f.write(b'        LCD_ShowNum(track_minus[i].x + track_minus[i].w + 10, ly + 10,\n')
    f.write(b'                    param_vals[i], 4, WHITE, BG_COLOR, 16);\n')
    f.write(b'        LCD_DrawButtonCN(track_plus[i].x, track_plus[i].y,\n')
    f.write(b'                         track_plus[i].w, track_plus[i].h, "+", BTN_TEXT, DARKGRAY);\n')
    f.write(b'    }\n')
    f.write(b'    /* \xBF\xEC\xCB\xD9\xC2\xFD\xCA\xB1\xB0\xE6 */\n')
    f.write(b'    {\n')
    f.write(b'        uint16_t preset_w = 150, preset_h = 35;\n')
    f.write(b'        uint16_t preset_gap = 20;\n')
    f.write(b'        uint16_t total_preset_w = 3 * preset_w + 2 * preset_gap;\n')
    f.write(b'        uint16_t preset_sx = (800 - total_preset_w) / 2;\n')
    f.write(b'        uint16_t preset_y = 345;\n')
    f.write(b'        for (i = 0; i < 3; i++) {\n')
    f.write(b'            track_preset_btns[i].x = preset_sx + i * (preset_w + preset_gap);\n')
    f.write(b'            track_preset_btns[i].y = preset_y;\n')
    f.write(b'            track_preset_btns[i].w = preset_w;\n')
    f.write(b'            track_preset_btns[i].h = preset_h;\n')
    f.write(b'            LCD_DrawButtonCN(track_preset_btns[i].x, track_preset_btns[i].y,\n')
    f.write(b'                             track_preset_btns[i].w, track_preset_btns[i].h,\n')
    f.write(b'                             track_preset_names[i], BTN_TEXT, DARKGRAY);\n')
    f.write(b'        }\n    }\n')
    f.write(b'    LCD_ShowMixedString(30, TRACK_STATUS_Y, ' + b(CN_STATUS) + b', STATUS_COLOR, BG_COLOR);\n}\n')
    f.write(b'\n')
    # HandleTrackTouch
    f.write(b'static void HandleTrackTouch(uint16_t tx, uint16_t ty)\n{\n')
    f.write(b'    uint8_t i;\n')
    f.write(b'    for (i = 0; i < 2; i++) {\n')
    f.write(b'        if (PtInRect(tx, ty, &track_mode_btns[i])) {\n')
    f.write(b'            uint8_t j;\n            current_mode = i;\n            Motor_Stop();\n')
    f.write(b'            for (j = 0; j < 2; j++) {\n')
    f.write(b'                uint16_t c = (j == current_mode) ? BTN_ACTIVE : BTN_INACTIVE;\n')
    f.write(b'                LCD_DrawButtonCN(track_mode_btns[j].x, track_mode_btns[j].y,\n')
    f.write(b'                                 track_mode_btns[j].w, track_mode_btns[j].h,\n')
