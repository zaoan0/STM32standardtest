    f.write(b'static Touch_Data_t touch;\n')
    f.write(b'\n')
    f.write(b'#define MAIN_BTN_W   300\n')
    f.write(b'#define MAIN_BTN_H   100\n')
    f.write(b'#define MAIN_BTN_GAP 40\n')
    f.write(b'static Rect main_btns[PAGE_NUM];\n')
    f.write(b'static const char *main_btn_names[] = { "", ' + b(CN_TRACK_MODE) + b', ' + b(CN_GYRO_STRAIGHT) + b', ' + b(CN_BLANK1) + b', ' + b(CN_BLANK2) + b' };\n')
    f.write(b'\n')
    f.write(b'#define BACK_BTN_X   20\n')
    f.write(b'#define BACK_BTN_Y   10\n')
    f.write(b'#define BACK_BTN_W   80\n')
    f.write(b'#define BACK_BTN_H   36\n')
    f.write(b'static Rect back_btn;\n')
    f.write(b'\n')
    f.write(b'#define TRACK_BTN_Y      60\n')
    f.write(b'#define TRACK_BTN_H      70\n')
    f.write(b'#define TRACK_BTN_W      200\n')
    f.write(b'#define TRACK_BTN_GAP    40\n')
    f.write(b'#define TRACK_PARAM_Y    180\n')
    f.write(b'#define TRACK_PARAM_ROW  60\n')
    f.write(b'#define TRACK_PARAM_BTN  50\n')
    f.write(b'#define TRACK_PARAM_BH   36\n')
    f.write(b'#define TRACK_PARAM_VW   70\n')
    f.write(b'#define TRACK_PARAM_LX   180\n')
    f.write(b'#define TRACK_STATUS_Y   395\n')
    f.write(b'#define TRACK_PARAM_NUM  3\n')
    f.write(b'\n')
    f.write(b'static Rect track_mode_btns[2];\n')
    f.write(b'static Rect track_minus[TRACK_PARAM_NUM];\n')
    f.write(b'static Rect track_plus[TRACK_PARAM_NUM];\n')
    f.write(b'static const char *track_mode_names[] = { ' + b(CN_STOP) + b', ' + b(CN_TRACK) + b' };\n')
    f.write(b'static const char *track_param_names[] = { "Base", "Slight", "Strong" };\n')
    f.write(b'static Rect track_preset_btns[3];\n')
    f.write(b'static const char *track_preset_names[] = { ' + b(CN_FAST) + b', ' + b(CN_MED) + b', ' + b(CN_SLOW) + b' };\n')
    f.write(b'/* \xBF\xEC: 350/30/60, \xD6\xD0: 250/30/70, \xC2\xFD: 180/10/50 */\n')
    f.write(b'static const int16_t track_preset_vals[3][3] = {\n')
    f.write(b'    {350, 30, 60},\n')
    f.write(b'    {250, 30, 70},\n')
    f.write(b'    {180, 10, 50}\n')
