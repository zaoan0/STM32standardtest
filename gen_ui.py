CN_GYRO_STRAIGHT= b'\xCD\xD3\xC2\xDD\xD2\xC7\xD6\xB1\xCF\xDF'  # 陀螺仪直线
CN_BLANK1       = b'\xB4\xFD\xBF\xAA\xB7\xA2  1'  # 待开发  1
CN_BLANK2       = b'\xB4\xFD\xBF\xAA\xB7\xA2  2'  # 待开发  2
CN_BACK         = b'\xB7\xB5\xBB\xD8'  # 返回
CN_STOP         = b'\xCD\xA3\xD6\xB9'  # 停止
CN_TRACK        = b'\xD1\xAD\xBC\xA3'  # 循迹
CN_STATUS       = b'\xD7\xB4\xCC\xAC:'  # 状态:
CN_MODE         = b'\xC4\xA3\xCA\xBD:'  # 模式:
CN_ANGLE        = b'\xBD\xC7\xB6\xC8:'  # 角度:
CN_LOST         = b'\xB6\xAA\xCA\xA7:'  # 丢失:
CN_YES          = b'\xCA\xC7'  # 是
CN_NO           = b'\xB7\xF1'  # 否
CN_RUNNING      = b'\xD4\xCB\xD0\xD0\xD6\xD0'  # 运行中
CN_STOPPED      = b'\xD2\xD1\xCD\xA3\xD6\xB9'  # 已停止
CN_OUTPUT       = b'\xCA\xE4\xB3\xF6:'  # 输出:
CN_COMING_SOON  = b'\xBE\xB4\xC7\xEB\xC6\xDA\xB4\xFD...'  # 敬请期待...
CN_DEV1         = b'\xB4\xFD\xBF\xAA\xB7\xA2 1'  # 待开发 1
CN_DEV2         = b'\xB4\xFD\xBF\xAA\xB7\xA2 2'  # 待开发 2
CN_FAST         = b'\xBF\xEC'  # 快
CN_MED          = b'\xD6\xD0'  # 中
CN_SLOW         = b'\xC2\xFD'  # 慢

def b(s):
    """Wrap bytes in a C string literal."""
    return b'"' + s + b'"'

with open('USER/ui.c', 'wb') as f:
    f.write(b'#include "ui.h"\n')
    f.write(b'#include "tracking.h"\n')
    f.write(b'#include "lcd.h"\n')
    f.write(b'#include "touch.h"\n')
    f.write(b'#include "motor.h"\n')
    f.write(b'#include "debug.h"\n')
    f.write(b'\n')
    f.write(b'#define BG_COLOR       BLACK\n')
    f.write(b'#define TITLE_COLOR    CYAN\n')
    f.write(b'#define BTN_ACTIVE     GREEN\n')
    f.write(b'#define BTN_INACTIVE   DARKGRAY\n')
    f.write(b'#define BTN_TEXT       WHITE\n')
    f.write(b'#define PARAM_LABEL    LIGHTGRAY\n')
    f.write(b'#define STATUS_COLOR   YELLOW\n')
    f.write(b'\n')
    f.write(b'typedef struct { uint16_t x, y, w, h; } Rect;\n')
    f.write(b'\n')
    f.write(b'static Page_t current_page = PAGE_MAIN;\n')
